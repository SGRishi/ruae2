const DARK_HINTS = ['night', 'dark', 'storm', 'supercell', 'mist', 'snowy', 'fighting'];
const LIGHT_HINTS = ['light', 'spring', 'soft', 'white', 'bright', 'flowers'];
const NATURE_HINTS = ['nature', 'mountain', 'forest', 'sea', 'coast', 'lake', 'bee', 'flower', 'hummingbird'];

function normalizeTags(tags) {
  const unique = new Set();
  for (const tag of tags) {
    const clean = String(tag || '').trim().toLowerCase();
    if (!clean) continue;
    unique.add(clean);
  }
  return [...unique];
}

function hasKeyword(text, keywords) {
  const haystack = String(text || '').toLowerCase();
  return keywords.some((keyword) => haystack.includes(keyword));
}

function tagsFor500px(url) {
  const basename = String(url || '').split('/').pop() || '';
  const tags = ['500px'];

  if (hasKeyword(basename, NATURE_HINTS)) tags.push('nature');
  if (hasKeyword(basename, DARK_HINTS)) tags.push('dark');
  if (hasKeyword(basename, LIGHT_HINTS)) tags.push('light');

  if (!tags.includes('nature') && !tags.includes('dark') && !tags.includes('light')) {
    tags.push('nature');
  }

  return tags;
}

function catalogFromUrls(urls, source, tagsFactory) {
  const list = [];
  const seen = new Set();

  for (const url of urls) {
    const href = String(url || '').trim();
    if (!href || seen.has(href)) continue;
    seen.add(href);
    const tags = normalizeTags(typeof tagsFactory === 'function' ? tagsFactory(href) : tagsFactory);
    list.push({
      url: href,
      source,
      tags,
    });
  }

  return list;
}

const BACKGROUND_CATALOG = [
  ...catalogFromUrls(LEGACY_BACKGROUND_IMAGES, 'unsplash', ['legacy', 'nature', 'light']),
  ...catalogFromUrls(UNSPLASH_DARK_IMAGES, 'unsplash', ['dark']),
  ...catalogFromUrls(UNSPLASH_LIGHT_IMAGES, 'unsplash', ['light']),
  ...catalogFromUrls(UNSPLASH_NATURE_IMAGES, 'unsplash', ['nature']),
  ...catalogFromUrls(FIVE_HUNDRED_PX_IMAGES, '500px', tagsFor500px),
];

function scoreForPack(image, packKey) {
  const tags = new Set(image.tags || []);

  if (packKey === 'legacy') {
    return tags.has('legacy') ? 100 : 0;
  }

  if (packKey === 'dark') {
    let score = 0;
    if (tags.has('dark')) score += 10;
    if (tags.has('nature')) score += 3;
    if (image.source === '500px') score += 2;
    return score;
  }

  if (packKey === 'light') {
    let score = 0;
    if (tags.has('light')) score += 10;
    if (tags.has('nature')) score += 3;
    if (image.source === '500px') score += 1;
    return score;
  }

  if (packKey === 'nature') {
    let score = 0;
    if (tags.has('nature')) score += 10;
    if (tags.has('light')) score += 2;
    if (tags.has('dark')) score += 2;
    if (image.source === '500px') score += 1;
    return score;
  }

  if (packKey === 'mixed') {
    let score = 5;
    if (tags.has('nature')) score += 3;
    if (tags.has('dark')) score += 2;
    if (tags.has('light')) score += 2;
    if (image.source === '500px') score += 2;
    return score;
  }

  return 0;
}

function rankPack(packKey, maxItems = 200) {
  const scored = BACKGROUND_CATALOG
    .map((image, index) => ({
      image,
      score: scoreForPack(image, packKey),
      index,
    }))
    .filter((row) => row.score > 0)
    .sort((a, b) => b.score - a.score || a.index - b.index);

  const ranked = [];
  const seen = new Set();

  for (const row of scored) {
    if (seen.has(row.image.url)) continue;
    seen.add(row.image.url);
    ranked.push(row.image.url);
    if (ranked.length >= maxItems) break;
  }

  return ranked;
}

const ALL_UNIQUE_URLS = (() => {
  const seen = new Set();
  const out = [];
  for (const item of BACKGROUND_CATALOG) {
    if (seen.has(item.url)) continue;
    seen.add(item.url);
    out.push(item.url);
  }
  return out;
})();

